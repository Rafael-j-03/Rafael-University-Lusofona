montanhas = [150,200,400,800,100,250,120,550,600]

montanha_maior = max(montanhas)
montanha_menor = min(montanhas)

print ("A montanha mais alta é a montanha " + str(montanhas.index(montanha_maior) +1) + " e tem " + str(montanha_maior) + " metros")
print ("A montanha mais baixa é a montanha " + str(montanhas.index(montanha_menor) +1) + " e tem " + str(montanha_menor) + " metros")

